  function mostrarArchivos(categoria) {
    const contenedor = document.getElementById("archivos-container");
    const tarjetas = document.querySelector(".cards");
    const btnRegresar = document.getElementById("btn-regresar");
    const lista = archivos[categoria];
    if (!lista) return;
  
    // Oculta tarjetas y muestra bot贸n
    tarjetas.style.display = "none";
    btnRegresar.style.display = "inline-block";
  
    // Mostrar archivos
    let html = `<h2>Archivos - ${formatearNombre(categoria)}</h2><ul>`;
    lista.forEach(archivo => {
      html += `<li><a href="${archivo.link}" target="_blank">${archivo.nombre}</a></li>`;
    });
    html += `</ul>`;
  
    contenedor.innerHTML = html;
  }
  
  function volverInicio() {
    const contenedor = document.getElementById("archivos-container");
    const tarjetas = document.querySelector(".cards");
    const btnRegresar = document.getElementById("btn-regresar");
  
    // Limpiar y mostrar interfaz principal
    contenedor.innerHTML = "";
    tarjetas.style.display = "grid";
    btnRegresar.style.display = "none";
  }
  
